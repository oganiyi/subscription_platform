    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'website' => 'required|exists:websites,id',
            'title' => [
                'required', 'string', function ($attribute, $value, $fail) use ($request) {
                    $exist = Post::where(['website_id' => $request->website, 'title' => $value])->exists();

                    if ($exist) {
                        $fail("The specified title already exist on this website.");
                    }
                }
            ],
            'description' => 'required|string',
        ]);

        Post::create([
            'website_id' => $request->website,
            'title' => $request->title,
            'description' => $request->description
        ]);

        return response([
            'success' => true,
            'message' => "Post created successfully"
        ], 200);
    }

